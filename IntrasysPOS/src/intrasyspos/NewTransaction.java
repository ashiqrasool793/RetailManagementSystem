    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        DefaultTableModel model = (DefaultTableModel)jTransactionTable1.getModel();
        int[] selectedRows = jTransactionTable1.getSelectedRows();
        if (selectedRows.length > 0) {
            for (int i = selectedRows.length - 1; i >= 0; i--) {
                double oldTotal = (Double)model.getValueAt(selectedRows[i], 6);
                Long deleteItemId = (Long)model.getValueAt(selectedRows[i], 1);
                cart.remove(deleteItemId);
                model.removeRow(selectedRows[i]);
                for(int j=i; j<model.getRowCount(); j++) {
                    while((int)model.getValueAt(j, 0)!=1)
                    model.setValueAt(((int)model.getValueAt(j, 0)-1), j, 0);
                }
                grandTotal /= (1+taxRate);
                grandTotal /= (1-membershipDiscount);
                grandTotal -= oldTotal;
                discAmt = (grandTotal*membershipDiscount);
                grandTotal *= (1-membershipDiscount);
                tax = grandTotal*taxRate;
                grandTotal *= (1+taxRate);
                discountField.setText(df.format(discAmt));
                jTaxTextField3.setText(df.format(tax));
                jGrandTotalTextField3.setText(df.format(grandTotal));
                subtotal.setText(df.format(grandTotal/1.07));
            }
        }
    }//GEN-LAST:event_jButton6ActionPerformed

