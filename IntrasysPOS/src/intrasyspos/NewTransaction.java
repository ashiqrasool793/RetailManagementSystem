    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        int quantity=1;
        
        String itemId = jSearchItemTextField1.getText();
        ArrayList<Object> itemDetails = getItem(Long.parseLong(itemId));
        DefaultTableModel model = (DefaultTableModel)jTransactionTable1.getModel();
        //row, id, name, price, quantity, discount, total price
        //model.addRow(new Object[]{row, itemDetails.get(0),itemDetails.get(1),itemDetails.get(2),quantity,0,10.0}); 
        //row++;
        
       
        int rowCount = model.getRowCount();
        int existingProductRowIndex = -1;
        row = rowCount+1;

        for (int i = 0; i < rowCount; ++i) {
            if (model.getValueAt(i, 1) != null && model.getValueAt(i, 1).equals(itemDetails.get(0))) {
                existingProductRowIndex = i;
                break;
            }
        }
        

        if (existingProductRowIndex > -1) {
            int originalQuantity = Integer.parseInt(model.getValueAt(existingProductRowIndex, 4).toString());
            double itemPrice = Double.parseDouble(model.getValueAt(existingProductRowIndex, 3).toString());
            model.setValueAt((originalQuantity+1), existingProductRowIndex, 4);
            quantity = originalQuantity+1;
            double discountAmt = checkBulkDiscountAmount(Long.parseLong(itemId), quantity);
            double aBDiscount = checkItemAItemBDiscount((Long)itemDetails.get(0), cart);
            aBDiscount /=100;
            aBDiscount *= itemPrice;
            double initialTotal = Double.parseDouble(model.getValueAt(existingProductRowIndex, 6).toString());
            double updTotal = (100-discountAmt)/100*itemPrice*(originalQuantity+1) - aBDiscount;
            model.setValueAt(((discountAmt)/100*itemPrice*(originalQuantity+1)+aBDiscount), existingProductRowIndex, 5);
            model.setValueAt(updTotal, existingProductRowIndex, 6);
            computeIncreaseInQty(updTotal, initialTotal);
            /*grandTotal += (updTotal-initialTotal);
            tax += (updTotal-initialTotal)*taxRate;
            double itemTax = (updTotal-initialTotal)*taxRate;
            grandTotal += itemTax;
            jTaxTextField3.setText(df.format(tax));
            jGrandTotalTextField3.setText(df.format(grandTotal));*/
        } else {
            double price = (Double)itemDetails.get(2);
            double discountAmt = checkBulkDiscountAmount(Long.parseLong(itemId), quantity);
            double aBDiscount = checkItemAItemBDiscount((Long)itemDetails.get(0), cart);
            discountAmt /= 100;
            aBDiscount /=100;
            double totalPrice = quantity*price;
            double disc = totalPrice*discountAmt + totalPrice*aBDiscount;
            totalPrice -= disc;
            model.addRow(new Object[]{row, itemDetails.get(0),itemDetails.get(1),itemDetails.get(2),quantity,disc,totalPrice});
            cart.add((Long)itemDetails.get(0));
            grandTotal /= (1+taxRate);
            grandTotal += totalPrice;
            tax = grandTotal*taxRate;
            grandTotal *= (1+taxRate);
            jTaxTextField3.setText(df.format(tax));
            jGrandTotalTextField3.setText(df.format(grandTotal));
            subtotal.setText(df.format(grandTotal/1.07));
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

